        if(isset($request->q)) {
            $posts = Post::where('name', 'LIKE', '%'.$request->q.'%')->orWhere('content', 'LIKE', '%'.$request->q.'%')->orderBy('created_at', 'desc')->paginate(100);
        } else {
            $posts = Post::orderBy('created_at', 'desc')->paginate(50);
        }
        return view('posts.index', compact('posts'));
    }

